package main

import "fmt"

//比較演算子

func main() {
	// true
	fmt.Println(1 == 1)
	// false
	fmt.Println(1 == 2)
	// true
	fmt.Println(4 <= 8)
	// false
	fmt.Println(1 >= 8)
	// true
	fmt.Println(1 < 8)
	// true
	fmt.Println(3 > 1)
	// false
	fmt.Println(true == false)
	// true
	fmt.Println(true != false)
}
